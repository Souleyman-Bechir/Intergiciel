package com.intergiciel.enrollmentservice.dto;

/**
 * DTO représentant un étudiant dans le microservice d'inscription.
 */
public class StudentDto {
    private Long id;
    private String nom;
    private String prenom;
    private String email;

    public StudentDto() {
    }
 
    public StudentDto(Long id, String nom, String prenom, String email) {
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
